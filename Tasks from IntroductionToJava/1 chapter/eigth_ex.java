public class eigth_ex{
	public static void main(String[] args) {
		double radius = 5.5;
		double perimeter = radius * 2 * Math.PI;
		double area = radius * radius * Math.PI;

		System.out.println("Perimeter of circle with radius 5.5 is " + perimeter);
		System.out.println("Area of circle with radius 5.5 is " + area);
	}
}